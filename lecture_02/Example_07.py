# comparison operators
x = 2
y = 3

if x > y: # False
    print("True")
else:
    print("False")

if x < y: # True
    print("True")
else:
    print("False")

t = 4
z = 4

if t == z: #True
    print("True")
else:
    print("False")


if t != z: #False
    print("True")
else:
    print("False")


d = 5
g = 2

if d >= g: #True
    print("True")

if d <= g: # False
    print("True")
else:
    print("False")

h = 5
f = 5

if h <= f: # True
    print("True")

if h >= f: # True
    print("True")


